export function getVenueRolloutCategory(category: VenueRolloutCategory) {
  return VENUE_ROLLOUT_PRIORITY.filter((venue) => venue.category === category).sort(
    (a, b) => a.categoryOrder - b.categoryOrder
  );
}

export function getSeededVenueRolloutNodes() {
  return VENUE_ROLLOUT_PRIORITY.filter((venue) => venue.status === "seeded").sort(
    (a, b) => a.overallRank - b.overallRank
  );
}

export function getTopVenueRolloutNodes(limit = 10) {
  return VENUE_ROLLOUT_PRIORITY.slice()
    .sort((a, b) => a.overallRank - b.overallRank)
    .slice(0, limit);
}
