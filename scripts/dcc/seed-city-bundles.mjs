function ensureCityAliases() {
  const filePath = path.join(ROOT, "data", "city-aliases.json");
  const aliases = readJson(filePath);
  let changed = false;

  for (const seed of citySeeds) {
    if (!aliases[seed.slug]) {
      aliases[seed.slug] = `${seed.slug}-guide`;
      changed = true;
    }
  }

  if (!changed) return null;
  const sorted = Object.fromEntries(Object.entries(aliases).sort(([a], [b]) => a.localeCompare(b)));
  writeJson(filePath, sorted);
  return path.relative(ROOT, filePath);
}

function ensureCitiesIndex() {
  const filePath = path.join(ROOT, "data", "cities", "index.json");
  const index = readJson(filePath);
  const seen = new Set(index.cities.map((city) => city.slug));
  let changed = false;

  for (const seed of citySeeds) {
    if (seen.has(seed.slug)) continue;
    index.cities.push({
      slug: seed.slug,
      name: seed.name,
      state: seed.state,
      timezone: seed.timezone,
      centroid: seed.centroid,
      default_walk_radius_m: seed.walkRadius,
      launch_status: seed.launchStatus,
      density_profile: seed.densityProfile,
      modes: seed.modes,
    });
    changed = true;
  }

  if (!changed) return null;
  index.updated_at = "2026-03-26T12:00:00-06:00";
  index.cities.sort((a, b) => a.name.localeCompare(b.name));
  writeJson(filePath, index);
  return path.relative(ROOT, filePath);
}

